## > PRINTING / STDOUT

### - Common
print('Hello, World!')
# Hello, World


### - String Concatenation
print('Bl' 'ob')
# Blob

print('tomato' + 'sauce')
# tomatosauce


### - Separated String
print('Andy','and','Curl')
# Andy and Curl

print('Benny','Anna',sep=' loves ')
# Benny loves Anna


### - Formatted
# >> Value Concatenation
print('Corry has', 7-5, 'phones')
# Corry has 2 phones

# >> f-string
print(f'Emma is {2025-2006} years old')
# Emma is 19 years old

# >> .format() Function
print('There are {} kites in the sky'.format(5))
# There are 5 kites in the sky

# >> C-based % Format
print('The price was $%d' % (5))
# The price was $5